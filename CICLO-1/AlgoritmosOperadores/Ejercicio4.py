'''
    Dados los días de trabajo de una persona y el valor por 
    hora. Calcular su salario y mostrarlo en pantalla.
'''
print('Este programa calcula el salario de una persona según horas y costo por hora')
hora = int(input('Ingrese las horas trabajadas: '))
costo = int(input('Ingrese el valor de la hora laboral: '))
salario = hora * costo
print('El salario es: ', salario)